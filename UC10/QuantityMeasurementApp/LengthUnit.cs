using System;

namespace QuantityMeasurementApp
{
    // Enum representing different units of length
    public enum LengthUnit
    {
        // Base unit for length
        Feet,

        // 1 foot = 12 inches
        Inch,

        // 1 yard = 3 feet
        Yard,
        
        // Metric unit (1 cm = 0.0328084 feet)
        Centimeter
    }
}