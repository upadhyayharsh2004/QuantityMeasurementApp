using System;

namespace QuantityMeasurementApp
{
    // Enum representing different units of weight measurement
    public enum WeightUnit
    {
        // Base unit for weight
        Kilogram,

        // 1 Gram = 0.001 Kilogram
        Gram,

        // 1 Pound ≈ 0.453592 Kilogram
        Pound
    }
}